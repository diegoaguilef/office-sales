    public void calculatePrice(int qtty) {
        int price = Integer.parseInt(stxPrice.getText()),
                total;
        double iva, net;
        total = qtty * price;
        iva = total * 0.19;
        net = total - iva;
        txtNet.setText(String.valueOf(net));
        txtIva.setText(String.valueOf(iva));
        txtTotal.setText(String.valueOf(total));
    }

    public boolean hasStock() {
        int stock, qtty;
        qtty = Integer.parseInt(txtQtt.getText());
        stock = stxStock.getText().equals("") ? 0 : Integer.parseInt(stxStock.getText());

        return stock != 0 && qtty <= stock;
    }

