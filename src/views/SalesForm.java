    private void comboArticlesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboArticlesActionPerformed
        String name;
        Article article = null;
        if (comboArticles.getSelectedItem() != null
                && comboArticles.getSelectedIndex() != 0) {
            txtQtt.setEnabled(true);
            name = String.valueOf(comboArticles.getSelectedItem());
            article = ArticleDAO.findByName(name);
            if (article != null) {
                stxName.setText(article.getName());
                stxStock.setText(String.valueOf(article.getStock()));
                stxPrice.setText(String.valueOf(article.getPrice()));
            } else {
                JOptionPane.showMessageDialog(null, "Hubo un error al seleccionar artículo", "Información", 0);
            }
        } else {
            txtQtt.setEnabled(false);
        }
    }//GEN-LAST:event_comboArticlesActionPerformed

