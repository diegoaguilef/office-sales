    private void txtQttKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQttKeyReleased
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        int code = evt.getKeyCode();
        int price, qtty = 0;
        if (!(Character.isDigit(c) || code == KeyEvent.VK_BACKSPACE || code == KeyEvent.VK_DELETE)
            || code == KeyEvent.VK_SPACE) {
            getToolkit().beep();
            evt.consume();
        } else {
            if (!txtQtt.getText().trim().equals("")) {
                txtQtt.setText(txtQtt.getText().trim());
                qtty = Integer.parseInt(txtQtt.getText());
                calculatePrice(qtty);
            } else {
                txtNet.setText("");
                txtIva.setText("");
                txtTotal.setText("");
            }
        }
    }//GEN-LAST:event_txtQttKeyReleased

